// A linked list is a non-contiguous list of data points/nodes
// where each previous node references the next node via pointer
//
// Each linked list had data stored, and also a pointer which points to the next node.
//
// The LinkedList is best at storing a list of data that is highly variable since the data
// is non-linear, and it can make adjustments easier. LinkedLists are not good for 
// data that needs to be searched over often since the data is not contiguous and 
// you must start at the head and search linearly to the tail.
//
// Doubly LinkedLists are LinkLists with nodes that have two pointers instead of one.
// One pointer will point to the previous node, and one pointer will point to the next node.
